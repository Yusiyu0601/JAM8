        public static Image fft(Bitmap bitmap)
        {
            var fft1 = new Complex[bitmap.Width, bitmap.Height];
            Gaussian gau = new();
            for (int j = 0; j < bitmap.Height; j++)
                for (int i = 0; i < bitmap.Width; i++)
                    fft1[i, j] = new Complex(bitmap.GetPixel(i, j).R, 0);
            fft1 = MyFFT.fft2(fft1);

            GridProperty gp = GridProperty.create(GridStructure.create_simple(bitmap.Width, bitmap.Height, 1));
            for (int j = 0; j < bitmap.Height; j++)
                for (int i = 0; i < bitmap.Width; i++)
                {
                    int value = (int)fft1[i, j].Real;
                    gp.set_value(i + 1, j + 1, value);
                }
            DataMapper dm = new();
            dm.Reset((double)gp.Min, (double)gp.Max, 0, 255);
            for (int n = 0; n < gp.gridStructure.N; n++)
            {
                gp.set_value(n, (float?)dm.MapAToB(gp.get_value(n).Value));
            }

            Bitmap result = new(bitmap.Width, bitmap.Height);
            for (int j = 0; j < bitmap.Height; j++)
                for (int i = 0; i < bitmap.Width; i++)
                {
                    int value = (int)gp.get_value(i + 1, j + 1);
                    result.SetPixel(i, j, Color.FromArgb(value, value, value));
                }

            ImageProcess.Show(result);
            return result;

        }

