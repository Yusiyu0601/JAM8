        /// <returns></returns>
        public static Bitmap create_gray(int Width, int Height)
        {
            Bitmap Bmp = new(Width, Height, PixelFormat.Format8bppIndexed);
            System.Drawing.Imaging.ColorPalette Pal = Bmp.Palette;
            for (int Y = 0; Y < Pal.Entries.Length; Y++)
                Pal.Entries[Y] = Color.FromArgb(255, Y, Y, Y);
            Bmp.Palette = Pal;
            return Bmp;
        }

        /// <summary>
        /// 将彩色图转换为灰度图
        /// </summary>
        /// <param name="Src"></param>
        /// <returns></returns>
        public static Bitmap color_to_gray(Bitmap Src)
        {
            Bitmap b = new(Src);
            LockBitmap lockbmp = new(b);
            lockbmp.LockBits();//锁定Bitmap，通过Pixel访问颜色
            for (int i = 0; i < b.Width; i++)//循环读取像素转换灰度值
            {
                for (int j = 0; j < b.Height; j++)
                {
                    Color curColor = lockbmp.GetPixel(i, j);
                    int ret = (int)(curColor.R * 0.299 + curColor.G * 0.587 + curColor.B * 0.114);
                    lockbmp.SetPixel(i, j, Color.FromArgb(ret, ret, ret));
                }
            }

            //从内存解锁Bitmap
            lockbmp.UnlockBits();
            return b;
        }

